# 10 moves into the variable types of people. 
types_of_people = 10

# This string calls the variable (types_of_people) and assigns it to the variable x.
x = "There are #{types_of_people} types of people."

# The string binary is assigned to the variable binary.
binary = "binary"

# The string don't is assigned to the variable do_not.
do_not = "don't"

# The string uses the variables binary and do_not. "Those who know binary and those who don't."
y = "Those who know #{binary} and those who #{do_not}."

# prints the x variable
puts x

# prints the y variable
puts y

# prints the string with the variable x.
puts "I said: #{x}."

# prints the string with the variable y.
puts "I also said: '#{y}'."

# assigns false to the variable hilarious.
